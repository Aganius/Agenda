public class TablaConversion {

    public static float pesoADolar(float peso, float tasa) {
        return peso * tasa;
    }

    public static float pesoAEuro(float peso, float tasa) {
        return peso * tasa;
    }

    public static float dolarAPeso(float dolar, float tasa) {
        return dolar * tasa;
    }

    public static float dolarAEuro(float dolar, float tasa) {
        return dolar * tasa;
    }

    public static float euroAPeso(float euro, float tasa) {
        return euro * tasa;
    }

    public static float euroADolar(float euro, float tasa) {
        return euro * tasa;
    }
}
